// Internationalization manager
class I18nManager {
  constructor() {
    this.currentLang = localStorage.getItem('language') || 'en';
    this.translations = translations;
  }

  // Get translation for a key
  t(key) {
    const keys = key.split('.');
    let value = this.translations[this.currentLang];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return value || key;
  }

  // Change language
  setLanguage(lang) {
    if (this.translations[lang]) {
      this.currentLang = lang;
      localStorage.setItem('language', lang);
      this.updatePage();
      
      // Update HTML lang attribute
      document.documentElement.lang = lang;
    }
  }

  // Get current language
  getCurrentLanguage() {
    return this.currentLang;
  }

  // Update all translatable elements on the page
  updatePage() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
      const key = element.getAttribute('data-i18n');
      const translation = this.t(key);
      
      if (element.tagName === 'INPUT' && element.type === 'submit') {
        element.value = translation;
      } else {
        element.textContent = translation;
      }
    });

    // Update language toggle button
    this.updateLanguageToggle();
  }

  // Update language toggle button text
  updateLanguageToggle() {
    const toggleBtn = document.getElementById('language-toggle');
    if (toggleBtn) {
      toggleBtn.textContent = this.currentLang === 'en' ? 'ES' : 'EN';
      toggleBtn.setAttribute('title', this.currentLang === 'en' ? 'Cambiar a EspaÃ±ol' : 'Switch to English');
      console.log(`ğŸ”„ Language toggle updated to: ${toggleBtn.textContent}`);
    }
  }

  // Initialize i18n system
  init() {
    // Set initial HTML lang attribute
    document.documentElement.lang = this.currentLang;
    
    // Setup language toggle when navigation is ready
    this.setupLanguageToggle();
    
    // Update page with current language
    this.updatePage();
  }

  // Setup language toggle button
  setupLanguageToggle() {
    const checkForButton = () => {
      const toggle = document.getElementById('language-toggle');
      if (toggle) {
        // Button found, set it up
        this.updateLanguageToggle();
        toggle.addEventListener('click', () => {
          const newLang = this.currentLang === 'en' ? 'es' : 'en';
          this.setLanguage(newLang);
        });
        console.log('âœ… Language toggle button configured');
        return true;
      }
      return false;
    };

    // Try immediately
    if (!checkForButton()) {
      // If not found, check periodically
      const interval = setInterval(() => {
        if (checkForButton()) {
          clearInterval(interval);
        }
      }, 500); // Check every 500ms
      
      // Stop checking after 5 seconds
      setTimeout(() => {
        clearInterval(interval);
        console.log('âš ï¸ Language toggle button not found after 5 seconds');
      }, 5000);
    }
  }

}

// Global i18n instance
const i18n = new I18nManager();

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
  i18n.init();
});
